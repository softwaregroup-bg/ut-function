const {or, xor, and} = require('./');
const tap = require('tap');

tap.equals(or(), undefined, 'no arguments returns undefined');
tap.equals(xor(), undefined, 'no arguments returns undefined');
tap.equals(and(), undefined, 'no arguments returns undefined');

tap.equals(or([
    '11111111111111112222222222222222',
    '33333333333333334444444444444444'
]), '33333333333333336666666666666666', 'or two elements');

tap.equals(or([
    '11111111111111112222222222222222',
    '33333333333333334444444444444444',
    '55555555555555556666666666666666'
]), '77777777777777776666666666666666', 'or three elements');

tap.equals(xor([
    '11111111111111112222222222222222',
    '33333333333333334444444444444444'
]), '22222222222222226666666666666666', 'xor two elements');

tap.equals(xor([
    '11111111111111112222222222222222',
    '33333333333333334444444444444444',
    '55555555555555556666666666666666'
]), '77777777777777770000000000000000', 'xor three elements');

tap.equals(and([
    '11111111111111112222222222222222',
    '33333333333333334444444444444444'
]), '11111111111111110000000000000000', 'and two elements');

tap.equals(and([
    '11111111111111112222222222222222',
    '33333333333333334444444444444444',
    '55555555555555556666666666666666'
]), '11111111111111110000000000000000', 'and three elements');
